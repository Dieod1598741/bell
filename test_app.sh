#!/bin/bash
# 빌드된 앱 테스트 스크립트

echo "=========================================="
echo "Bell 앱 실행 테스트"
echo "=========================================="

APP_PATH="dist/Bell.app/Contents/MacOS/Bell"

if [ ! -f "$APP_PATH" ]; then
    echo "❌ 실행 파일을 찾을 수 없습니다: $APP_PATH"
    exit 1
fi

echo "실행 파일 경로: $APP_PATH"
echo ""
echo "터미널에서 실행 중..."
echo "=========================================="
echo ""

# 실행 파일에 실행 권한 부여
chmod +x "$APP_PATH"

# 터미널에서 실행 (에러 메시지 확인)
"$APP_PATH" 2>&1

echo ""
echo "=========================================="
echo "실행 완료 (또는 오류 발생)"
echo "=========================================="



